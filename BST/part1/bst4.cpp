/*Print in Range*/
#include<bits/stdc++.h>
using namespace std;
class Node{
public:
    int data;
    Node* left;
    Node* right;
    Node(int data){
        this->data=data;
        left=right=NULL;
    }
};
Node* insert(Node* root,int val){
    if(root==NULL){
        root=new Node(val);
        return root;
    }
    
    if(root->data > val){
        root->left=insert(root->left,val);
    }else{
        root->right=insert(root->right,val);
    }
    return root;
}
Node* buildBST(int arr[],int n){
    Node* root=NULL;

    for(int i=0; i<n; i++){
        root=insert(root,arr[i]);
    }
    return root;
}
void inorder(Node* root){
    if(root==NULL){
        return;
    }
    inorder(root->left);
    cout<<root->data<<" ";
    inorder(root->right);
}
void printINrange(Node* root,int start,int end){
    if(root == NULL) return;

    
    if(start <= root->data && root->data <= end){//case1 : root is b/w range
        cout<<root->data<<" ";
        printINrange(root->left,start,end);
        printINrange(root->right,start,end);
    }
    
    if(root->data < start){//case2 : root < start
        printINrange(root->right,start,end);
    }
    if(root->data > end){//case3 : root > end
        printINrange(root->left,start,end);
    }
}
int main(){
    int arr[]={8,5,3,1,4,6,10,11,14};
    Node* root=buildBST(arr,9);
    inorder(root);
    cout<<endl;
    printINrange(root,5,12);
    return 0;
}